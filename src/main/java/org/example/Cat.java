package org.example;

/**
 * ============================================
 * КЛАСС CAT - УРОК О НАСЛЕДОВАНИИ (ПРИНЦИП №2)
 * ============================================
 * 
 * Cat НАСЛЕДУЕТСЯ от Animal. Это значит:
 * 1. Cat имеет ВСЕ методы и поля Animal (eat(), sleep(), hungerLevel)
 * 2. Cat может ПЕРЕОПРЕДЕЛИТЬ методы (makeSound(), play())
 * 3. Cat - это Animal, но Animal - это НЕ Cat
 * 
 * Аналогия наследования:
 * - Animal = родитель (батя)
 * - Cat = ребенок (сын)
 * - Сын получает наследство (методы и поля), но может делать что-то по-своему
 * 
 * extends = "расширяет". Cat расширяет Animal новым функционалом.
 */
public class Cat extends Animal {
    
    // ============================================
    // УНИКАЛЬНЫЕ ПОЛЯ КОТА
    // ============================================
    // Это поле есть ТОЛЬКО у кота, а не у всех животных.
    // Демонстрирует, что наследник может добавлять свой функционал.
    private boolean isLazy = true; // котики ленивые по умолчанию

    // ============================================
    // КОНСТРУКТОР
    // ============================================
    // super() вызывает конструктор родительского класса (Animal)
    // Если не вызвать super(), компилятор попытается вызвать super() без параметров
    public Cat() {
        super(7); // создаем кота с голодом 7 (коты всегда немного голодные)
    }

    public Cat(int initialHunger) {
        super(initialHunger); // передаем голод родительскому конструктору
    }

    // ============================================
    // ПЕРЕОПРЕДЕЛЕНИЕ АБСТРАКТНЫХ МЕТОДОВ
    // ============================================
    // @Override - аннотация, говорит "я переопределяю метод родителя"
    // Это не просто совпадение имен - это явное переопределение.
    // 
    // Полиморфизм (Принцип №3) в действии:
    // Animal animal = new Cat();
    // animal.makeSound(); // вызовет метод КОТА, а не базовый!
    
    @Override
    public void makeSound() {
        System.out.println("Мяу! (котик требует еду)");
    }

    @Override
    public void play() {
        System.out.println("Кот дерется с тенью (классика!)");
        isLazy = false; // после игры котик уже не ленивый... на 5 минут
    }

    // ============================================
    // УНИКАЛЬНЫЙ МЕТОД КОТА
    // ============================================
    // Этот метод есть ТОЛЬКО у кота.
    // Если у тебя переменная типа Animal, ты не сможешь вызвать этот метод
    // без приведения типа (casting).
    
    public void knockThingsOffTable() {
        System.out.println("Кот скинул что-то со стола. Потому что может.");
        isLazy = false; // активность!
    }

    public boolean isLazy() {
        return isLazy;
    }
}
